public class ConstantConfig {
    public static  String APP_DEVICE = "";
    public static  String APP_KEY = "";
    public static String APP_VERSION_CODE = "";
    public static String APP_UUID = "";

    public static void setAppDevice(String appDevice) {
        APP_DEVICE = appDevice;
    }

    public static void setAppKey(String appKey) {
        APP_KEY = appKey;
    }

    public static void setAppVersionCode(String appVersionCode) {
        APP_VERSION_CODE = appVersionCode;
    }

    public static void setAppUuid(String uuid) {
        APP_UUID = uuid;
    }
}
